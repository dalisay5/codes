def data_cleanse(lst):
    for i in range(len(lst)):
        if lst[i] == '<1\n' or lst[i] == '<1':
            lst[i] = 0
        else:
            lst[i] = int(float(lst[i]))
#opens file and cleans the data
def data_open(lst):
    file_open = open(lst)
    file_list = file_open.readlines()
    data_cleanse(file_list)
