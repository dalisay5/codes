#Make a population pyramid 
def pyramid(x,y,list_m,list_f):
    #Space between blocks
    h = 24
    fill1 = 'orange red'
    fill2 = 'dodger blue'

    g.goto(x,y)
    g.pendown()
    g.pencolor('white')
    g.left(90)

    for i in range(len(list_m)):
        #F
        g.fillcolor(fill2)
        g.begin_fill()
        g.forward(x*list_m[i])
        g.left(90)
        g.forward(h)
        g.left(90)
        g.forward(x*list_m[i])
        g.end_fill()
        #M
        g.fillcolor(fill1)
        g.begin_fill()
        g.forward(x*list_f[i])
        g.left(90)
        g.forward(h)
        g.left(90)
        g.forward(x*list_f[i])
        g.end_fill()
        g.left(90)
        g.forward(h)
        g.right(90)

        turtle.tracer(True)
        time.sleep(0.5)
        turtle.tracer(False)


